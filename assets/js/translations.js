// Language switching functionality
// Determine initial language from URL
function getLanguageFromURL() {
  const path = window.location.pathname;
  // Check if URL contains /zh (e.g., /zh, /zh/, /projects/billboard/zh)
  if (path.match(/\/zh\/?$/)) {
    return 'zh-TW';
  }
  return 'en';
}

let currentLang = getLanguageFromURL();

function setLanguage(lang, updateURL = true) {
  currentLang = lang;
  document.documentElement.lang = lang;
  
  // Update URL if needed
  if (updateURL) {
    const currentPath = window.location.pathname;
    let newPath = currentPath;
    
    if (lang === 'zh-TW') {
      // Add /zh to the path if not already there
      if (!currentPath.match(/\/zh\/?$/)) {
        // Remove trailing slash if present, then add /zh
        newPath = currentPath.replace(/\/$/, '') + '/zh';
        if (currentPath === '/' || currentPath === '') {
          newPath = '/zh';
        }
      }
    } else {
      // Remove /zh from the path
      newPath = currentPath.replace(/\/zh\/?$/, '');
      if (newPath === '') {
        newPath = '/';
      }
    }
    
    // Update URL without page reload
    if (newPath !== currentPath) {
      window.history.pushState({ lang: lang }, '', newPath);
    }
  }
  
  translatePage();
  // Always update the switcher after changing language
  updateLanguageSwitcher();
}

function translatePage() {
  if (currentLang === 'en') {
    // Reset to English (original content)
    document.querySelectorAll('[data-translate]').forEach(element => {
      const key = element.getAttribute('data-translate');
      // If data-original exists, use it; otherwise use innerHTML if data-translate-html is true, else textContent
      let originalText;
      if (element.hasAttribute('data-original')) {
        originalText = element.getAttribute('data-original');
      } else if (element.hasAttribute('data-translate-html')) {
        // On first load, preserve the original HTML
        originalText = element.innerHTML;
        element.setAttribute('data-original', originalText);
      } else {
        originalText = element.textContent;
        element.setAttribute('data-original', originalText);
      }
      
      if (element.tagName === 'A' && element.hasAttribute('data-translate-href')) {
        // For links, restore original href
        element.href = element.getAttribute('data-original-href') || element.href;
      }
      
      if (element.hasAttribute('data-translate-html')) {
        element.innerHTML = originalText;
      } else {
        element.textContent = originalText;
      }
    });
    return;
  }
  
  const langTranslations = translations[currentLang];
  if (!langTranslations) return;
  
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    const translation = langTranslations[key];
    
    if (translation) {
      // Store original if not already stored
      if (!element.hasAttribute('data-original')) {
        if (element.hasAttribute('data-translate-html')) {
          element.setAttribute('data-original', element.innerHTML);
        } else {
          element.setAttribute('data-original', element.textContent);
        }
      }
      
      // For links with href translations
      if (element.tagName === 'A' && element.hasAttribute('data-translate-href')) {
        if (!element.hasAttribute('data-original-href')) {
          element.setAttribute('data-original-href', element.href);
        }
        // Keep original href for now, can be extended if needed
      }
      
      // Apply translation
      if (element.hasAttribute('data-translate-html')) {
        element.innerHTML = translation;
      } else {
        element.textContent = translation;
      }
    }
  });
  
  // Update language switcher button
  updateLanguageSwitcher();
}

function updateLanguageSwitcher() {
  const switcher = document.getElementById('language-switcher');
  if (!switcher) {
    // Button not found yet, try again after a short delay
    setTimeout(updateLanguageSwitcher, 100);
    return;
  }
  
  // Show the opposite language option (if current is EN, show 中 to switch to Chinese)
  // When currentLang is 'en', show 中 (to switch to Chinese)
  // When currentLang is 'zh-TW', show EN (to switch to English)
  if (currentLang === 'en' || currentLang === null || currentLang === undefined) {
    switcher.textContent = '中';
    switcher.setAttribute('aria-label', '切換到繁體中文');
  } else if (currentLang === 'zh-TW') {
    switcher.textContent = 'EN';
    switcher.setAttribute('aria-label', 'Switch to English');
  }
}

function toggleLanguage() {
  if (currentLang === 'en') {
    setLanguage('zh-TW');
  } else {
    setLanguage('en');
  }
}

// Initialize translation on page load
document.addEventListener('DOMContentLoaded', function() {
  // Get language from URL (defaults to English)
  currentLang = getLanguageFromURL();
  translatePage();
  updateLanguageSwitcher();
});

// Handle browser back/forward buttons
window.addEventListener('popstate', function(event) {
  currentLang = getLanguageFromURL();
  translatePage();
  updateLanguageSwitcher();
});

